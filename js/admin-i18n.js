const ADMIN_DEFAULT_LANG = 'en';

function getAdminLang() {
  const saved = localStorage.getItem('bluewelcome_admin_lang');
  if (saved && ADMIN_STRINGS[saved]) return saved;
  return ADMIN_DEFAULT_LANG;
}

function setAdminLang(code) {
  if (!ADMIN_STRINGS[code]) return;
  localStorage.setItem('bluewelcome_admin_lang', code);
  applyAdminTranslations();
}

function at(key) {
  const lang = getAdminLang();
  return (ADMIN_STRINGS[lang] && ADMIN_STRINGS[lang][key]) || ADMIN_STRINGS.en[key] || key;
}

function applyAdminTranslations() {
  document.querySelectorAll('[data-t]').forEach(elm => {
    elm.textContent = at(elm.getAttribute('data-t'));
  });
  document.querySelectorAll('[data-t-ph]').forEach(elm => {
    elm.setAttribute('placeholder', at(elm.getAttribute('data-t-ph')));
  });
  document.querySelectorAll('[data-t-title]').forEach(elm => {
    elm.setAttribute('title', at(elm.getAttribute('data-t-title')));
  });
  document.documentElement.lang = getAdminLang();
}
