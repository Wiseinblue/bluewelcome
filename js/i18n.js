// Lancio: SOLO inglese (standard del turismo). L'ospite che vuole un'altra lingua
// usa il "Traduci pagina" del proprio browser (gratis) — per questo <html lang="en">.
// Le traduzioni IT/DE/EL restano sopra in UI_STRINGS: riattivabili aggiungendole qui.
const SUPPORTED_LANGUAGES = [
  { code: 'en', label: 'English', flag: '🇬🇧' },
];

const DEFAULT_LANG = 'en';

// Accetta solo lingue effettivamente attive nel selettore (SUPPORTED_LANGUAGES),
// così una lingua salvata in passato ma ora disattivata ricade su EN.
function isActiveLang(code) {
  return !!code && SUPPORTED_LANGUAGES.some(l => l.code === code) && !!UI_STRINGS[code];
}

function getCurrentLang() {
  const saved = localStorage.getItem('bluewelcome_lang');
  if (isActiveLang(saved)) return saved;
  const configLang = window.STAY_CONFIG?.property?.language;
  if (isActiveLang(configLang)) return configLang;
  return DEFAULT_LANG;
}

function setLang(code) {
  if (!UI_STRINGS[code]) return;
  localStorage.setItem('bluewelcome_lang', code);
  if (window.applyTranslations) window.applyTranslations();
}

function t(key) {
  const lang = getCurrentLang();
  return (UI_STRINGS[lang] && UI_STRINGS[lang][key]) || UI_STRINGS[DEFAULT_LANG][key] || key;
}
